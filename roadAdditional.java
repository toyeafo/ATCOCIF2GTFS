package example;

/**
 *
 * @author AFROGENIUS
 */
public class roadAdditional extends roadElement {

    private String locationId;

    private double lat;

    private double lon;

    public roadAdditional() {
        super(roadElement.Type.ADDITIONAL_LOCATION);
    }

    public String getLocationId() {
        return locationId;
    }

    public void setLocationId(String locationId) {
        this.locationId = locationId;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}
