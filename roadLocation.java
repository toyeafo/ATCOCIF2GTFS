package example;

/**
 *
 * @author AFROGENIUS
 */
public class roadLocation extends roadElement {

    private String locationId;

    private String name;

    public roadLocation() {
        super(roadElement.Type.LOCATION);
    }

    public String getLocationId() {
        return locationId;
    }

    public void setLocationId(String locationId) {
        this.locationId = locationId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
